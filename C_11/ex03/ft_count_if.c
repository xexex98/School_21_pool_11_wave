int	ft_count_if(char	**tab, int	length, int(*f)(char*))
{
	int	i;
	int	a;

	i = 0;
	a = 0;
	while (tab[i])
	{
		if (f(tab[i]) != 0)
			a++;
		i++;
	}
	return (a);
}
