#include <unistd.h>

void	print(char	c)
{	
	write(1, &c, 1);
}

void	ft_putstr(char	*str)
{
	int	w;

	w = 0;
	while (str[w] != '\0')
	{
		print(str[w]);
		w++;
	}
}
