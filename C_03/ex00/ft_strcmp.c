int	ft_strcmp(char	*s1, char	*s2)
{
	int	i;
	int	y;

	i = 0;
	y = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[y] != '\0')
		y++;
	if (i < y)
		return (i - y);
	else if (i > y)
		return (i - y);
	else
		return (0);
}
