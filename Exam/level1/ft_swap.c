#include <stdio.h>

void	ft_swap(int	*a, int	*b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

int	main(void)
{
	int	a;
	int	b;

	a = 1111123133;
	b = 5;
	ft_swap(&a, &b);
	printf("%d %d", a, b);
}
